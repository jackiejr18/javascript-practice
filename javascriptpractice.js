// Logical Operator OR (||)

/* 

Returns true even if one of the mulltiple operands is true

true || true // true
true || false // true
false || true // true
false || false // false 

*/ 

let a = 5;
let b = 10;

console.log(a > 0 || b > 0);
console.log(a > 0 || b < 0);
console.log(a < 0 || b > 0);
console.log(a < 0 || b < 0);